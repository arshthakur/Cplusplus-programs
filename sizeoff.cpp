#include<iostream>
using namespace std;
int main()
{
    int a=56;
    char b='c';
    float c=45.5L;
    long double d=45.456f;
    cout<<"the value of a is "<<sizeof(a)<<endl;
    cout<<"the value of b is "<<sizeof(b)<<endl;
    cout<<"the value of c is "<<sizeof(c)<<endl;
    cout<<"the value of d is "<<sizeof(d)<<endl;
    return 0;
};
